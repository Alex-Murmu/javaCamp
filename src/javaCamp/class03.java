package javaCamp;

public class class03 {
	public static void main(String[] args) {
		
		        // Declaration and initialization of variables
		        int age = 30; // An integer variable named "age" initialized with the value 30
		        double salary = 50000.50; // A double variable named "salary" initialized with a decimal value
		        char grade = 'A'; // A character variable named "grade" initialized with the character 'A'
		        boolean isStudent = true; // A boolean variable named "isStudent" initialized with the value true
		        String name = "John Doe"; // A string variable named "name" initialized with a string value

		        // Output the values of the variables
		        System.out.println("Age: " + age);
		        System.out.println("Salary: " + salary);
		        System.out.println("Grade: " + grade);
		        System.out.println("Is Student: " + isStudent);
		        System.out.println("Name: " + name);
		        System.out.println("We declare variables of different types: int, double, char, boolean, and String.\r\n"
		        		+ "\r\n"
		        		+ "We initialize these variables with specific values.\r\n"
		        		+ "\r\n"
		        		+ "We use System.out.println to print the values of these variables to the console.");
		    }
		

	}


